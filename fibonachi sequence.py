seq_num = int(input("enter how many times would you like to go forward in fibonacci seq : "))

fn_1 = 0
fn_2 = 1
fn_3 = fn_1 + fn_2
i = 2

if seq_num == 0 :
    print("its not valid")
elif seq_num == 1 :
    print(fn_3)
elif seq_num == 2 :
    print(fn_3,"\n",fn_3)
else :
    print(fn_3)
    while i <= seq_num :
        i += 1
        fn_3 = fn_1 + fn_2
        print(fn_3)
        fn_1 = fn_2
